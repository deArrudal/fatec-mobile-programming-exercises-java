package com.example.geometricshape.model;

public class Rectangle {
    private float rectangleBase;
    private float rectangleHeight;

    public Rectangle() {
        super();
    }

    public float getRectangleBase() {
        return rectangleBase;
    }

    public void setRectangleBase(float rectangleBase) {
        this.rectangleBase = rectangleBase;
    }

    public float getRectangleHeight() {
        return rectangleHeight;
    }

    public void setRectangleHeight(float rectangleHeight) {
        this.rectangleHeight = rectangleHeight;
    }
}
